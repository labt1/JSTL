package JavaBean;

public class Info {
	String nombre;
	String apellido;
	
	public String get_nombre() {
		return nombre;
	}
	
	public void set_nombre(String nombre) {
		this.nombre=nombre;
	}
	
	public String get_apellido() {
		return apellido;
	}
	
	public void set_apellido(String apellido) {
		this.apellido=apellido;
	}
}
